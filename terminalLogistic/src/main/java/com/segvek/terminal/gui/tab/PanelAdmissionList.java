    @Override
    public void edit() {
        int rows[] = jTable1.getSelectedRows();
        for(int i=0; i<rows.length; i++){
            Admission a = (Admission)jTable1.getValueAt(rows[i], 0);
            MainFrame.getInstance().addPanelTab("Завоз №"+a.getId(), new PanelAdmission(a));
        }
    }
    
    
    
    
    
    
