    @Override
    public void init() {
        try {
            List<Admission> list = service.getAllAdmission();
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            for(Admission a:list){
                dtm.addRow(new Object[]{a,a.getContract().getNumber(),null,a.getTank().getNumber(),a.getVolume(),a.getBegin(),a.getDrainLocation().getNumber()
                ,a.getFactBegin(),a.getFactEnd(),null});
            }
        } catch (ServiceException ex) {
            Logger.getLogger(PanelAdmissionList.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(jPanel1, "Не удалось загрузить план завоза, для испрвления ошибки обратитесь к администратору\n"+ex.getMessage());
        }
    }
    
    

